int count = 0;
foreach (var kvp in seedExpansions)
{
    var worddataPath = Path.Combine(worddataDir, $"{kvp.Key}.yaml");

    if (!File.Exists(worddataPath))
    {
        Console.WriteLine($"SKIP: {kvp.Key}.yaml not found at {worddataPath}");
        continue;
    }

    var yaml = File.ReadAllText(worddataPath);
    var lines = yaml.Split('\n').ToList();

    // Find function_words section
    var fwStart = -1;
    var fwEnd = -1;
    var lemmasStart = -1;

    for (int i = 0; i < lines.Count; i++)
    {
        var trimmed = lines[i].Trim();
        if (trimmed.StartsWith("function_words:"))
        {
            fwStart = i;
        }
        else if (trimmed.StartsWith("lemmas:"))
        {
            lemmasStart = i;
            if (fwStart >= 0 && fwEnd < 0)
                fwEnd = i;
        }
        else if (fwStart >= 0 && fwEnd < 0 && trimmed.StartsWith("verbs:"))
        {
            fwEnd = i;
        }
        else if (fwStart >= 0 && fwEnd < 0 && !string.IsNullOrWhiteSpace(trimmed) && !trimmed.StartsWith("- ") && trimmed.Length > 0 && char.IsLetter(trimmed[0]))
        {
            // Another top-level key
            fwEnd = i;
        }
    }
    if (fwEnd < 0) fwEnd = lines.Count;

    // Keep everything before and after function_words section
    var header = lines.Take(fwStart + 1).ToList();
    var footer = lines.Skip(fwEnd).ToList();

    // Build new function_words set
    var existingWords = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
    if (fwStart >= 0)
    {
        for (int i = fwStart + 1; i < fwEnd; i++)
        {
            var trimmed = lines[i].Trim();
            if (trimmed.StartsWith("- "))
            {
                var word = trimmed.Substring(2).Trim();
                if (!string.IsNullOrEmpty(word) && word != "True" && word != "False" && word != "true" && word != "false")
                    existingWords.Add(word);
            }
        }
    }

    // Add expansion words
    foreach (var w in kvp.Value)
        existingWords.Add(w);

    // Write new content
    using var writer = new StreamWriter(worddataPath, false, new System.Text.UTF8Encoding(false));
    foreach (var line in header)
    {
        if (!string.IsNullOrEmpty(line))
            writer.WriteLine(line.TrimEnd());
    }
    foreach (var w in existingWords.OrderBy(x => x))
    {
        writer.WriteLine($"  - {w}");
    }
    foreach (var line in footer)
    {
        writer.WriteLine(line.TrimEnd());
    }

    Console.WriteLine($"Updated {kvp.Key}.yaml: {existingWords.Count} function words");
    count++;
}

Console.WriteLine($"Done! Updated {count} files.");
