<#
.SYNOPSIS
    Creates seed wordlist JSON files for languages NOT covered by stopwords-iso.
#>
param(
    [string]$OutputDir = (Join-Path (Join-Path $PSScriptRoot "..") "wordlists")
)

$ErrorActionPreference = "Stop"
if (!(Test-Path $OutputDir)) { New-Item -ItemType Directory -Path $OutputDir -Force | Out-Null }

function New-SeedWordlist {
    param([string]$iso3, [string]$iso1, [string]$name, [string[]]$words)
    $words = $words | ForEach-Object { $_.Trim().ToLowerInvariant() } | Where-Object { $_ -ne '' } | Sort-Object -Unique
    $output = [ordered]@{
        iso3 = $iso3
        iso1 = $iso1
        function_words = @($words)
        metadata = [ordered]@{
            generated = (Get-Date -Format "yyyy-MM-dd")
            source = "seed"
            word_count = $words.Count
        }
    }
    $json = $output | ConvertTo-Json -Depth 3
    $outPath = Join-Path $OutputDir "$iso3.json"
    $json | Set-Content -Path $outPath -Encoding UTF8
    Write-Host "  $iso3 ($iso1): $($words.Count) words" -ForegroundColor Yellow
}

